<h1>Preface</h1>
<p>
An introductory passage written by the author,
a preface relates how and why the book came into being,
or provides context for the current edition.
</p>

<h1>“What's the difference between a preface and an introduction?</h1>
<p>
A preface is personal to the author, discussing why they wrote the book
and what their process was.
</p>
<p>
An introduction relates directly to the subject matter and really kicks things off
— which is why it's part of the body, not the front matter.”
</p>