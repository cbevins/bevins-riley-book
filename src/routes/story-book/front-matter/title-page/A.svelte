<h1>Title Page</h1>
<p>
The title page features the full title of the book and the author's name
— as they appear on the cover — and any potential subtitle,
translator, as well as the logo or name of the publisher.
</p>
<p>
It can be plain or ornamental, depending on the publisher and author,
and will always go on the right hand side of a spread
(also known as recto).
</p>