<h1>Introduction (for nonfiction)</h1>
<p>
An introduction is a few pages that usher the reader into the subject matter.
The intro goes over early events or information related to the main narrative,
so the reader has a solid footing before they begin.
</p>

<h1>“What's the difference between a preface and an introduction?</h1>
<p>
A preface is personal to the author, discussing why they wrote the book
and what their process was.
</p>
<p>
An introduction relates directly to the subject matter and really kicks things off
— which is why it's part of the body, not the front matter.”
</p>

<h1>Prologue (for fiction)</h1>
<p>
The prologue is a section just before the novel begins that aims to set the stage
and intrigue the reader about the story they’re about to read.
Indeed, many prologues contain intriguing events that only become contextualized
and make sense later in the story.
For that reason, prologues are generally considered part of the body of the book,
and not one of the paratextual elements included in the front matter.
</p>