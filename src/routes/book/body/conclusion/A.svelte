<h1>Conclusion (for nonfiction)</h1>
<p>
A section that sums up the core ideas and concepts of the text.
Explicitly labeled conclusions are becoming less common in nonfiction books,
which commonly offer final thoughts in the last chapter instead,
but more academic theses may still be formatted this way.
</p>

<h1>Epilogue (for fiction)</h1>
<p>
A scene that wraps up the story in a satisfying manner,
an epilogue often takes place some time in the future,
relative to the events in the main chapters of the book.
Alternatively, if there are more books to come in the series,
the epilogue may raise new questions or hint at what will happen
in the next installment.  
</p>

<h1>Afterword</h1>
<p>
Any other final notes on the book;
can be written by the author or by someone they know.
</p>

<h1>Postscript</h1>
<p>
A brief final comment after the narrative comes to an end,
usually just a sentence or two
(e.g. “Matthew died at sea in 1807, but his memory lives on”).
</p>