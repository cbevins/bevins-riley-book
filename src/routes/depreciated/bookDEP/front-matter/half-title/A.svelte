<h1>Half-title</h1>
<p>
Also known as the bastard title page, the half-title page is a blank page
with nothing but the title of the book, written in a plain font without frills
or decorations. 
</p>
<p>Traditionally, when readers bought books without covers and added their own,
custom-made leather covers, the purpose of the half-title was to protect
the more ornamental title page and frontispiece (more on this later)
during the bookbinding process.
