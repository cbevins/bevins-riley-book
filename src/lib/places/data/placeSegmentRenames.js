export const Level3Map = new Map([
    ['sint antoniepolder', 'Sint Anthoniepolder'],
    ['sint antoniepolder', 'Sint Anthoniepolder'],
    ['sint antonie-polder', 'Sint Anthoniepolder'],
])
export function place1(key, missingValue=null) {
    const place = Level1Map.get(key.toLowerCase())
    return place ? place : (missingValue ? missingValue : key)
}

export function place2(key, missingValue=null) {
    const place = Level2Map.get(key.toLowerCase())
    return place ? place : fixups2(key, missingValue)
}

function fixups2(key, missingValue) {
    const k = key.toLowerCase()
    if (k === 'rotterdam') return 'ZUI'
    if (k === 'ouid-beijerland') return 'ZUI'
    return missingValue ? missingValue : '"'+key+'"'
}